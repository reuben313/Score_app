package domainmodel;


public class Standing {

private Team team;
private int win;
private int lose;
private int draw;
private int point;




public Team getTeam() {
	return team;
}
public void setTeam(Team team) {
	this.team = team;
}
public int getWin() {
	return win;
}
public void setWin(int win) {
	this.win = win;
}
public int getLose() {
	return lose;
}
public void setLose(int lose) {
	this.lose = lose;
}
public int getDraw() {
	return draw;
}
public void setDraw(int draw) {
	this.draw = draw;
}
public int getPoint() {
	return point;
}
public void setPoint(int point) {
	this.point = point;
}









}
